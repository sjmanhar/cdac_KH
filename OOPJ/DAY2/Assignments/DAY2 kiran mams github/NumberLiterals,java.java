public class NumberLiterals {
    public static void main(String[] args) {
	int binary = 0b1010;
	int octal = 012;
	int hexa = 0xA;
	double floating = 12.34;
	
	    System.out.println("Binary literal: " + binary);
        System.out.println("Octal literal: " + octal);
        System.out.println("Hexadecimal literal: " + hexa);
        System.out.println("Floating-point literal: " + floating);
	    }
}