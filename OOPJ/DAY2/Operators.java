import java.util.Scanner;
public class Operators {
	
	public static void main(String... args){
	
	Scanner sc = new Scanner(System.in);
	System.out.println("a: ");
	int a= sc.nextInt();
	System.out.println("b: ");
	int b= sc.nextInt();

		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
			
		
	}
}
