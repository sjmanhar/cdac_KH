public class Array {
	
    public static void main(String[] args)
    {

        // initializing array
        int[] arr = { 1, 8, 3, 7, 5 };

        // size of array
        int n = arr.length;

        // traversing array
        for (int i = 0; i < n; i++)
            System.out.print(arr[i] + " ");
    }
}